"""01 - 함수를 도구로 만들고(@tool), 모델에 도구를 알려 준다(bind_tools).

이 예제 하나만으로 다음을 익힙니다.
  - @tool로 평범한 함수를 LangChain 도구로 바꾼다.
  - @tool이 함수의 이름·docstring·타입 힌트에서 도구 메타데이터를 자동으로 뽑는 것을 본다.
  - 도구는 모델 없이 .invoke로 직접 실행해 볼 수 있다.
  - bind_tools로 모델에 "이런 도구들을 쓸 수 있다"고 알려 준다 (아직 실행은 안 한다).

이 파일은 자기완결입니다. 다른 파일이나 main에 의존하지 않으며, 아래 한 줄로 단독 실행됩니다.
  uv run python 03_tool_calling/01_define_and_bind.py

STEP 1·2는 모델 없이도 동작합니다. STEP 3의 bind_tools는 모델 객체가 필요하므로
키가 없으면 안내만 출력하고 종료합니다 (문법·import 점검은 키 없이도 됩니다).
"""

# import는 "다른 파일(라이브러리)에 있는 기능을 끌어와 이 파일에서 쓰겠다"는 선언입니다.
# os는 파이썬에 기본 내장된 모듈로, 환경변수(컴퓨터에 저장된 설정값)를 읽을 때 씁니다.
import os

# from A import B 는 "A라는 모듈에서 B만 골라 가져온다"는 뜻입니다.
from dotenv import load_dotenv
from langchain.chat_models import init_chat_model
# @tool은 함수를 도구로 바꾸는 데코레이터입니다. v1 권장 경로는 langchain_core.tools 입니다.
from langchain_core.tools import tool

# .env 파일이 있으면 환경변수로 읽어 들입니다 (로컬 실행 시 키를 .env에 둡니다).
# 함수 이름 뒤의 ( )는 "그 함수를 지금 실행하라"는 표시입니다.
load_dotenv()

# MODEL은 우리가 직접 만든 변수(값을 담아 두는 이름표)입니다. 대문자 이름은 "고정값(상수)"이라는 관례입니다.
# 모델은 "벤더:모델명" 문자열 하나로 지정합니다. 이 줄만 바꾸면 전체 코드가 다른 모델을 씁니다.
# google-genai로 전환하려면: MODEL = "google-genai:gemini-3.5-flash" (GOOGLE_API_KEY 필요)
MODEL = "openai:gpt-5.4-mini"


# ===========================================================================
# 도구 정의 — @tool이 함수를 도구로 바꿉니다.
# ===========================================================================

# @로 시작하는 줄은 "데코레이터"입니다. 바로 아래 함수를 감싸 새 능력을 입힙니다.
# @tool을 붙이면 평범한 함수가 LangChain 도구 객체로 바뀝니다.
#   - 함수 이름(add)은 도구 이름이 됩니다.
#   - docstring("""...""")은 도구 설명이 되어, 모델이 "언제 이 도구를 쓸지" 판단하는 유일한 근거가 됩니다.
#   - 타입 힌트(a: int 등)는 모델이 인자를 어떤 형식으로 채울지 알려 주는 스키마가 됩니다.
# 모델이 실제로 보는 것은 함수 "본문"이 아니라 이 세 가지(이름·설명·인자 형식)뿐입니다.
@tool
def add(a: int, b: int) -> int:
    """두 정수를 더한다."""  # 이 docstring이 그대로 도구 설명으로 모델에 전달됩니다
    return a + b


@tool
def multiply(a: int, b: int) -> int:
    """두 정수를 곱한다."""
    return a * b


# ===========================================================================
# STEP 1 — @tool이 함수에서 뽑는 메타데이터를 본다 (모델 없이).
# ===========================================================================

def step1_inspect_tool_metadata() -> None:
    """@tool이 함수에서 어떤 메타데이터를 자동으로 뽑는지 본다."""
    # 도구 객체는 .name·.description·.args 속성으로 모델에 전달될 메타데이터를 들고 있습니다.
    # 객체.속성 형태로 그 객체가 가진 정보를 꺼냅니다.
    print("[name]", add.name)         # 예: add  (함수 이름이 도구 이름이 됨)
    print("[desc]", add.description)  # 예: 두 정수를 더한다.  (docstring이 설명이 됨)
    print("[args]", add.args)         # 예: {'a': {'title': 'A', 'type': 'integer'}, 'b': {...}}

    # 체크포인트: name·description·args가 함수에서 자동 추출되어 출력되면 성공입니다.
    #   docstring을 비워 두면 모델이 도구의 쓸모를 모르게 됩니다. 설명은 늘 명확히 적습니다.


# ===========================================================================
# STEP 2 — 도구는 모델 없이 코드에서 직접 실행해 볼 수도 있다.
# ===========================================================================

def step2_invoke_tool_directly() -> None:
    """도구 자체가 잘 동작하는지 모델 없이 직접 확인한다."""
    # @tool로 감싼 함수는 .invoke({인자})로 직접 부를 수 있습니다.
    # 중괄호 { }는 딕셔너리(key: value 짝의 묶음)입니다. 인자 이름을 key로 값을 채웁니다.
    # 모델을 붙이기 전에 도구 자체를 단위 테스트하듯 점검할 때 유용합니다.
    print("[direct]", add.invoke({"a": 3, "b": 5}))  # 예: 8

    # 체크포인트: 모델 없이도 도구가 8을 돌려주면, 도구 자체는 준비된 것입니다.


# ===========================================================================
# STEP 3 — bind_tools로 모델에 도구 목록을 알려 준다 (아직 부르지는 않는다).
# ===========================================================================

def step3_bind_tools(model):
    """bind_tools로 모델에 도구 목록을 묶는다 (도구를 실행하지는 않는다)."""
    # bind_tools는 도구 목록을 모델에 묶어, 매 호출마다 도구들의 스키마를 함께 보내도록 합니다.
    # 원본 모델은 그대로 두고, 도구가 묶인 새 사본을 돌려줍니다.
    # 대괄호 [ ]는 리스트(값을 순서대로 담는 묶음)입니다. 도구 두 개를 담았습니다.
    model_with_tools = model.bind_tools([add, multiply])

    # 지금은 묶기만 했습니다. 반환된 객체가 무엇인지만 확인합니다.
    # __class__.__name__은 "이 객체의 클래스(종류) 이름"입니다.
    print("도구를 묶은 모델:", model_with_tools.__class__.__name__)  # 예: RunnableBinding

    # 체크포인트: 오류 없이 객체가 만들어지면, 모델에 도구를 알려 줄 준비가 끝난 것입니다.
    #   이 묶인 모델을 invoke하면 응답의 tool_calls로 호출 제안이 돌아옵니다(다음 예제 02에서 다룸).
    return model_with_tools


# ===========================================================================
# 실행 진입점 — STEP 1·2는 키 없이, STEP 3은 모델이 필요합니다.
# ===========================================================================

# def는 함수(여러 줄의 동작을 하나의 이름으로 묶은 것)를 정의하는 키워드입니다.
def main() -> None:
    print("=== STEP 1: 도구 메타데이터 관찰 (모델 없이) ===")
    step1_inspect_tool_metadata()

    print("\n=== STEP 2: 도구 직접 실행 (모델 없이) ===")
    step2_invoke_tool_directly()

    # if는 "조건이 참일 때만 안쪽 블록을 실행하라"는 분기문입니다.
    # os.getenv("이름")은 환경변수 값을 읽습니다. 없으면 None을 돌려줍니다.
    # not은 참/거짓을 뒤집습니다. 즉 "키가 없으면(not ...)" 안쪽을 실행합니다.
    if not os.getenv("OPENAI_API_KEY"):
        print("\n[STEP 3 건너뜀] OPENAI_API_KEY가 없어 모델 단계는 생략합니다.")
        print("  .env에 키를 넣고 다시 실행하면 STEP 3까지 동작합니다.")
        return  # return은 함수를 여기서 끝내고 빠져나간다는 뜻입니다.

    # 모델 객체를 한 번만 만들어 STEP 3에 건넵니다. init_chat_model은 "벤더:모델명"을 받아
    # 그 공급사에 맞는 모델 객체를 돌려줍니다(벤더별 클래스를 직접 import할 필요 없음).
    model = init_chat_model(MODEL)

    print("\n=== STEP 3: bind_tools로 도구 알려 주기 ===")
    step3_bind_tools(model)


# 아래 한 줄은 "이 파일을 직접 실행했을 때만 main()을 부른다"는 파이썬의 표준 관용구입니다.
if __name__ == "__main__":
    main()
